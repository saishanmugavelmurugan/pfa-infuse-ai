def get_missing_keys():
    """Get all missing keys for each language"""
    en_keys = set(TRANSLATIONS.get('en', {}).keys())
    missing = {}
    
    for lang in ['ar', 'th', 'vi', 'id', 'ms']:
        lang_keys = set(TRANSLATIONS.get(lang, {}).keys())
        missing[lang] = en_keys - lang_keys
    
    return missing

def fill_missing_translations():
    """Fill missing translations with appropriate values"""
    en_translations = TRANSLATIONS.get('en', {})
    missing = get_missing_keys()
    
    filled_translations = {}
    
    for lang, missing_keys in missing.items():
        lang_map = TRANSLATIONS_MAP.get(lang, {})
        filled_translations[lang] = {}
        
        for key in missing_keys:
            # First check if we have a direct translation
            if key in lang_map:
                filled_translations[lang][key] = lang_map[key]
            else:
                # Use English as fallback but mark it
                en_value = en_translations.get(key, key)
                filled_translations[lang][key] = en_value
        
        print(f"{lang.upper()}: Filled {len(filled_translations[lang])} missing translations")
    
    return filled_translations

def generate_update_code():
    """Generate Python code to update the language.py file"""
    filled = fill_missing_translations()
    
    print("\n=== Generated Update Code ===\n")
    
    for lang, translations in filled.items():
        print(f"\n# {lang.upper()} Updates")
        print(f"TRANSLATIONS['{lang}'].update({{")
        for key, value in sorted(translations.items()):
            # Escape quotes in value
            escaped_value = value.replace("'", "\\'").replace('"', '\\"')
            print(f'    "{key}": "{escaped_value}",')
        print("})")
    
    return filled

