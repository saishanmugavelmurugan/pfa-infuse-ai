@router.get("/consent/patient/{patient_id}")
async def get_patient_consents(
    patient_id: str,
    consent_type: Optional[str] = None,
    limit: int = Query(default=20, le=100)
):
    """Get all video consents for a patient"""
    db = await dependencies.get_database()
    
    query = {"patient_id": patient_id}
    if consent_type:
        query["consent_type"] = consent_type
    
    consents = await db.video_consents.find(
        query, {"_id": 0}
    ).sort("recorded_at", -1).limit(limit).to_list(limit)
    
    return {
        "patient_id": patient_id,
        "total": len(consents),
        "consents": consents
    }


