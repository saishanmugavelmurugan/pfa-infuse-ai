@router.get("/stats")
async def get_video_consulting_stats():
    """Get video consulting statistics"""
    db = await dependencies.get_database()
    
    total_sessions = await db.video_sessions.count_documents({})
    completed_sessions = await db.video_sessions.count_documents({"status": "completed"})
    total_consents = await db.video_consents.count_documents({})
    
    # Calculate average duration
    pipeline = [
        {"$match": {"status": "completed", "duration_seconds": {"$exists": True}}},
        {"$group": {"_id": None, "avg_duration": {"$avg": "$duration_seconds"}}}
    ]
    duration_result = await db.video_sessions.aggregate(pipeline).to_list(1)
    avg_duration = duration_result[0]["avg_duration"] if duration_result else 0
    
    return {
        "total_sessions": total_sessions,
        "completed_sessions": completed_sessions,
        "active_sessions": len(active_sessions),
        "total_consents_recorded": total_consents,
        "average_session_duration_minutes": round(avg_duration / 60, 1) if avg_duration else 0,
        "hms_integration_status": "pending_setup"
    }
