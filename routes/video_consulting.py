@router.put("/session/{session_id}/start")
async def start_video_session(session_id: str):
    """Mark a video session as started"""
    db = await dependencies.get_database()
    
    result = await db.video_sessions.update_one(
        {"id": session_id},
        {"$set": {
            "status": "active",
            "started_at": datetime.now(timezone.utc).isoformat()
        }}
    )
    
    if result.modified_count == 0:
        raise HTTPException(status_code=404, detail="Session not found")
    
    return {"message": "Session started", "session_id": session_id}


@router.put("/session/{session_id}/end")
async def end_video_session(session_id: str):
    """End a video session and record duration"""
    db = await dependencies.get_database()
    
    session = await db.video_sessions.find_one({"id": session_id})
    if not session:
        raise HTTPException(status_code=404, detail="Session not found")
    
    ended_at = datetime.now(timezone.utc)
    started_at = datetime.fromisoformat(session.get("started_at", ended_at.isoformat()).replace('Z', '+00:00'))
    duration = int((ended_at - started_at).total_seconds())
    
    await db.video_sessions.update_one(
        {"id": session_id},
        {"$set": {
            "status": "completed",
            "ended_at": ended_at.isoformat(),
            "duration_seconds": duration
        }}
    )
    
    # Remove from active sessions
    active_sessions.pop(session_id, None)
    
    return {
        "message": "Session ended",
        "session_id": session_id,
        "duration_seconds": duration,
        "recording_status": "pending_processing"
    }


