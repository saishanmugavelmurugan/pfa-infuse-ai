class LanguagePreference(BaseModel):
    user_id: Optional[str] = None
    region: str
    language_code: str
    country_code: Optional[str] = None

class UserLanguageSettings(BaseModel):
    id: str
    user_id: str
    region: str
    language_code: str
    updated_at: str

@router.get("/config")
async def get_language_config():
    """Get all supported regions and their languages"""
    return {
        "regions": LANGUAGE_CONFIG,
        "country_mapping": COUNTRY_TO_REGION,
        "supported_languages": ["en", "hi", "fr", "ar", "th", "vi", "id", "ms"]
    }

@router.get("/region/{country_code}")
async def get_region_by_country(country_code: str):
    """Get region and available languages for a country code"""
    country_upper = country_code.upper()
    region_key = COUNTRY_TO_REGION.get(country_upper, "other")
    region_config = LANGUAGE_CONFIG[region_key]
    
    return {
        "country_code": country_upper,
        "region_key": region_key,
        "region_name": region_config["region_name"],
        "languages": region_config["languages"],
        "default_language": region_config["default"]
    }

@router.post("/user-preference")
async def save_user_language_preference(preference: LanguagePreference):
    """Save user's language preference"""
    db = await get_db()
    
    settings = {
        "id": str(uuid4()),
        "user_id": preference.user_id or "anonymous",
        "region": preference.region,
        "language_code": preference.language_code,
        "country_code": preference.country_code,
        "updated_at": datetime.now(timezone.utc).isoformat()
    }
    
    # Upsert - update if exists, insert if not
    if preference.user_id:
        existing = await db.user_language_settings.find_one({"user_id": preference.user_id}, {"_id": 0})
        if existing:
            await db.user_language_settings.update_one(
                {"user_id": preference.user_id},
                {"$set": settings}
            )
        else:
            await db.user_language_settings.insert_one(settings)
    else:
        await db.user_language_settings.insert_one(settings)
    
    return {"success": True, "settings": settings}

@router.get("/user-preference/{user_id}")
async def get_user_language_preference(user_id: str):
    """Get user's saved language preference"""
    db = await get_db()
    
    settings = await db.user_language_settings.find_one(
        {"user_id": user_id},
        {"_id": 0}
    )
    
    if not settings:
        return {"found": False, "default": {"region": "other", "language_code": "en"}}
    
    return {"found": True, "settings": settings}

@router.get("/translations/{language_code}")
async def get_translations(language_code: str):
    """Get translations for a specific language"""
    # Return translations based on language code
    translations = TRANSLATIONS.get(language_code, TRANSLATIONS["en"])
    return {"language_code": language_code, "translations": translations}

